// Aprogram that will print a sequence of values using for loop

public class Lec6Page21Exercise1{

    public static void main(String[] args){
        int i;
        for(i=3;i<=23;i+=5){
            if(i<=22){
                System.out.print(i+",");
            }
            else{
                System.out.print(i);
            }
        }


    }		
}